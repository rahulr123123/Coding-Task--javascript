const oddEvenNumber = (a) =>{
    const result=[]
    let terms;
    if(a % 2 === 0){
        return terms = a-1
    }
    else{
        return terms;
    }

     for(let i=1;i<=terms;i++){
        result.push(2*i-1)
    }
    return result.join("")
   
}
